#include "stdafx.h"
#include "calculator.h"

float calculator::add(float firstNum, float secondNum)
{
	return firstNum + secondNum;
}

float calculator::sub(float firstNum, float secondNum)
{
	return firstNum - secondNum;
}

float calculator::mul(float firstNum, float secondNum)
{
	return firstNum*secondNum;
}

float calculator::div(float firstNum, float secondNum)
{
	return firstNum / secondNum;
}